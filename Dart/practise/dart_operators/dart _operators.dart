void main() {
  //.........dart-operators.....................
  // 1 arithmetic operators
  int numberOne = 12;
  int numberTwo = 5;
// addition
  int addition = numberOne + numberTwo;
  print(addition);

  // subtraction
  int subtraction = numberOne - numberTwo;
  print(subtraction);

  // divide
  double divide = numberOne / numberTwo;
  print(divide);

  // multiplication
  int multiplication = numberOne * numberTwo;
  print(multiplication);

  // modulus % //vagshes
  int modulus = numberOne % numberTwo;
  print(modulus);

  // division ~/ it's divided without doshomiker porer shongkha
  int division = numberOne ~/ numberTwo;
  print(division);

  // unary minus -(a-b)
  int unaryMinus = -(numberOne - numberTwo);
  print(unaryMinus);

  // 2. unary operators
  int value = 5;
  // Pre-increment (++x):
  // post-increment (x++):

  print(++value); // Pre-increment: value is now 6, Output: 6
  print(value++); // Post-increment: prints current value (6), then a becomes 7
  print(value); // Output: 7


  // Pre-decrement (--value)
  // post-decrement(value--)
  print(--value); // value is now 5 ,output : 4
  print(value--); // prints current value 4, then a becomes 3
  print(value); // print 3




}
