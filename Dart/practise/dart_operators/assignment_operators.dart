void main() {
  // 3.ASSIGNMENT OPERATORS
  //= assign operators
  int a = 8;
  int b = 4;
  double c = 4.0;

  // (+=) add and assign
  print(a += b); // a=a+b;
  //(-= ) subtraction and assign
  print(a -= b); // a=a-b;
  // multiplication and assign
  print(a *= 5); // a=a*5
  //divide and assign
  print(c /= 2); // c = c/2;
  // divide and assign
  print(a ~/= 2); // a = a~/2;
  //modulus and assign
  print(a %= 2); // a =a%2;
}
